//length traz o total numerico da string
let frase = "sou programador full Daiana"

console.log(frase.length);

//indexOf pesquisa na sting ou tals
console.log(frase.indexOf("full"));

//Slice, traz os caracatres que quer, tras a palavra não só o texto
console.log(frase.slice(4,20))

//trim, remover espaços em branco nos começos e final da string
console.log(frase.trim())

// split ,gerar um array apartir de uma string
console.log(frase.split())

//replace, para trocar uma palavra, só da primeira ocorrencia
console.log(frase.replace("full","tomara Full"))